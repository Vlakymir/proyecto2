     */
    public function store(Request $request)
    {
        $request->validate([
                'nombre' => 'required|max:255',
                'Precio_venta' => 'min:1',
                'stock' => 'min:0',
                'descripcion' => 'required|max:255',

        ]);
        Articulo::create($request->all());

        return redirect('/Articulo');
    }

